// Global translation functions
function changeLanguage(lang) {
    localStorage.setItem('preferred_language', lang);
    
    // Update all elements with data-i18n attribute across entire page
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else if (element.tagName === 'BUTTON') {
                element.textContent = translations[lang][key];
            } else if (element.tagName === 'OPTION') {
                element.textContent = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Update page title if it has data-i18n
    const pageTitle = document.querySelector('title[data-i18n]');
    if (pageTitle && translations[lang] && translations[lang][pageTitle.getAttribute('data-i18n')]) {
        document.title = translations[lang][pageTitle.getAttribute('data-i18n')];
    }
    
    showToast(`Language changed to ${lang === 'en' ? 'English' : lang === 'hi' ? 'Hindi' : 'Kannada'}`);
}

function initializeLanguage() {
    const savedLang = localStorage.getItem('preferred_language') || 'en';
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = savedLang;
    }
    changeLanguage(savedLang);
}

// Initialize when page loads
document.addEventListener('DOMContentLoaded', initializeLanguage);